func (s *stubFetcher) Fetch(_ context.Context, _ string) (*Result, error) {
	return nil, errors.New("stub")
}

func countOccurrences(s, sub string) int {
	count := 0
	for i := 0; i+len(sub) <= len(s); i++ {
		if s[i:i+len(sub)] == sub {
			count++
		}
	}
	return count
}
