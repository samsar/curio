func TestExtractVideoID(t *testing.T) {
	cases := []struct {
		name string
		url  string
		want string
		ok   bool
	}{
		{"standard", "https://www.youtube.com/watch?v=dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"no www", "https://youtube.com/watch?v=dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"mobile", "https://m.youtube.com/watch?v=dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"short link", "https://youtu.be/dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"shorts", "https://www.youtube.com/shorts/dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"live", "https://www.youtube.com/live/dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"embed", "https://www.youtube.com/embed/dQw4w9WgXcQ", "dQw4w9WgXcQ", true},
		{"with tracking", "https://www.youtube.com/watch?v=dQw4w9WgXcQ&si=abc&list=PL123", "dQw4w9WgXcQ", true},
		{"playlist only", "https://www.youtube.com/playlist?list=PL123", "", false},
		{"channel page", "https://www.youtube.com/@channel", "", false},
		{"homepage", "https://www.youtube.com/", "", false},
		{"not youtube", "https://example.com/watch?v=dQw4w9WgXcQ", "", false},
		{"youtu.be bare", "https://youtu.be/", "", false},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			u, err := url.Parse(tc.url)
			require.NoError(t, err)
			got, ok := urlutil.YouTubeVideoID(u)
			assert.Equal(t, tc.ok, ok)
			if ok {
				assert.Equal(t, tc.want, got)
			}
		})
	}
}

func TestParseVTT_Manual(t *testing.T) {
	vtt := `WEBVTT
Kind: captions
Language: en

00:00:01.000 --> 00:00:04.000
Welcome to this talk about databases.

00:00:04.500 --> 00:00:08.000
Today we'll cover indexing strategies.

00:00:08.500 --> 00:00:12.000
First, let's look at B-trees.

00:00:12.500 --> 00:00:16.000
B-trees are balanced tree structures.

00:00:16.500 --> 00:00:20.000
They provide O(log n) lookups.
`
	got := parseVTT([]byte(vtt))
	assert.Contains(t, got, "Welcome to this talk about databases.")
	assert.Contains(t, got, "B-trees are balanced tree structures.")
	assert.NotContains(t, got, "00:00")
	assert.NotContains(t, got, "WEBVTT")
}

func TestParseVTT_AutoGenerated(t *testing.T) {
	// Auto-generated VTT has <c> tags and rolling duplicate lines.
	vtt := `WEBVTT
Kind: captions
Language: en

00:00:00.000 --> 00:00:02.400 align:start position:0%
welcome<00:00:00.480><c> to</c><c> this</c><c> talk</c>

00:00:02.400 --> 00:00:04.800 align:start position:0%
welcome to this talk
about<00:00:02.880><c> databases</c>

00:00:04.800 --> 00:00:07.200 align:start position:0%
about databases
today<00:00:05.280><c> we</c><c> will</c><c> cover</c>
`
	got := parseVTT([]byte(vtt))
	assert.Contains(t, got, "welcome to this talk")
	assert.Contains(t, got, "about databases")
	assert.Contains(t, got, "today we will cover")
	// Should not contain duplicate "about databases" or HTML tags
	assert.NotContains(t, got, "<c>")
	assert.NotContains(t, got, "<00:00")
	assert.Equal(t, 1, countOccurrences(got, "about databases"))
}

func TestParseVTT_Empty(t *testing.T) {
	assert.Equal(t, "", parseVTT([]byte("WEBVTT\n\n")))
	assert.Equal(t, "", parseVTT([]byte("")))
}

